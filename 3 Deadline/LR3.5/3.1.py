def task_1(arr):
    for i in arr:
        if i == 100:
            return True
    return False
# Сложность: O(n) - в худшем случае нужно проверить все n элементов списка
# В среднем случае может быть быстрее, если 100 найден раньше, но Big O - это худший случай

def task_2(arr):
    return arr[0] + arr[-1]
# Сложность: O(1) - доступ к элементам массива по индексу занимает константное время
# Не зависит от размера массива, всегда выполняется за одно и то же время

def task_3(arr):
    count = 0
    for i in arr:
        for j in arr:
            if i == j:
                count += 1
    return count
# Сложность: O(n²) - вложенные циклы: для каждого из n элементов выполняем n итераций
# Всего n × n = n² операций сравнения